<p>Era o final do século e o cenário nas colônias de imigração alemã e italiana era de grande prosperidade.
 O porto fluvial de Caí, entre Porto Alegre e a região da serra gaúcha, dinamizava a distribuição 
 e comercialização dos produtos agropecuários produzidos pelos colonos e as mercadorias manufaturadas 
 vindas da capital. O transporte terrestre era feito precariamente por caixeiros viajantes, que percorriam
  grandes distâncias a cavalo, para vender seus produtos nos armazéns do interior do Estado, sendo a 
  banha um ítem de grande importância econômica. Adolfo Oderich, caixeiro-viajante, vislumbrou uma grande
   oportunidade de negócio e iniciou uma fábrica de refino de banha. 

<p>A tentativa de industrializar alimentos em conserva concretizou-se quando Carlos Henrique, o 
filho mais velho de Adolfo, retornou ao Brasil após três anos de estudos na Alemanha, trazendo o 
conhecimento da conservação de carnes.</p>


